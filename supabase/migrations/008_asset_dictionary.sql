-- Asset dictionary: replaces hardcoded STOCK_DICT, COIN_DICT, REAL_ESTATE_DICT
CREATE TABLE IF NOT EXISTS asset_dictionary (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    asset_name TEXT NOT NULL,        -- 삼성전자, 비트코인, 강남
    asset_code TEXT NOT NULL,        -- 005930, BTC, 서울/강남
    asset_type TEXT NOT NULL,        -- stock, coin, real_estate
    aliases TEXT[] DEFAULT '{}',     -- {"삼전", "삼성", "005930"}
    market TEXT,                     -- KOSPI, KOSDAQ, NYSE, BINANCE, etc.
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_asset_dict_name_type
    ON asset_dictionary(asset_name, asset_type);
CREATE INDEX IF NOT EXISTS idx_asset_dict_code
    ON asset_dictionary(asset_code);
CREATE INDEX IF NOT EXISTS idx_asset_dict_active
    ON asset_dictionary(is_active);

